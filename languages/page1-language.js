// check on load if there is a preference language
const currentLocation = `${window.location.origin}${window.location.pathname}`;

if(localStorage.getItem('preference') && window.location != `${currentLocation}#${localStorage.getItem('preference')}`) {
    window.location.replace(`${currentLocation}#${localStorage.getItem('preference')}`);
    window.location.reload();
};

// set preference language
const btns = document.querySelectorAll('[data-translation]');
btns.forEach(btn => {
    btn.addEventListener('click', (e) => {

        const preference = e.currentTarget.dataset.id;
        localStorage.setItem('preference', preference);

        window.location.replace(`${currentLocation}#${preference}`);
        window.location.reload();
    })
});

